class FontExporter:
    """Exports BDF fonts for ESPOS displays"""
    
    def __init__(self):
        from tools.bdf_font import BDFFont
        self.BDFFont = BDFFont
        self.loaded_fonts: Dict[str, 'BDFFont'] = {}
    
    def load_font(self, bdf_path: str, font_name: str) -> 'BDFFont':
        """Load BDF font file"""
        font = self.BDFFont.load(bdf_path)
        self.loaded_fonts[font_name] = font
        return font
    
    def export_font_subset(
        self, 
        font_name: str, 
        text_sample: str,
        output_path: Optional[str] = None
    ) -> str:
        """
        Export font with only characters used in text_sample
        
        Args:
            font_name: Name of loaded font
            text_sample: Text containing all needed characters
            output_path: Optional output file path
        
        Returns:
            Generated C code
        """
        from tools.bdf_font_export import export_font_subset
        
        if font_name not in self.loaded_fonts:
            raise ValueError(f"Font '{font_name}' not loaded")
        
        font = self.loaded_fonts[font_name]
        c_code = export_font_subset(font, text_sample, font_name)
        
        if output_path:
            Path(output_path).write_text(c_code, encoding="utf-8")
        
        return c_code
    
    def export_full_font(
        self,
        font_name: str,
        output_path: Optional[str] = None,
        charset: Optional[str] = None
    ) -> str:
        """
        Export complete font (or specific charset)
        
        Args:
            font_name: Name of loaded font
            output_path: Optional output file path
            charset: Optional charset string (default: ASCII printable)
        
        Returns:
            Generated C code
        """
        from tools.bdf_font_export import export_font_to_c
        
        if font_name not in self.loaded_fonts:
            raise ValueError(f"Font '{font_name}' not loaded")
        
        font = self.loaded_fonts[font_name]
        
        if charset is None:
            # Default: ASCII printable characters
            charset = "".join(chr(i) for i in range(32, 127))
        
        c_code = export_font_to_c(font, font_name, charset)
        
        if output_path:
            Path(output_path).write_text(c_code, encoding="utf-8")
        
        return c_code


