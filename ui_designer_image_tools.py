class IconBitmapExporter:
    """Exports UI icons as deduplicated C bitmap arrays"""
    
    def __init__(self):
        if not PIL_AVAILABLE:
            raise ImportError("PIL/Pillow required for bitmap export")
        
        from tools.xbmp_dedup import XBMPManager
        self.xbmp = XBMPManager(default_dither="floyd-steinberg")
        self.icon_refs: Dict[str, str] = {}  # name -> bitmap_ref
    
    def add_icon_from_file(self, icon_path: str, icon_name: str, size: int = 16) -> str:
        """
        Add icon from PNG/JPG file with resizing and dithering
        
        Args:
            icon_path: Path to image file
            icon_name: Name for C reference (e.g., "wifi", "battery")
            size: Target size in pixels (16, 24, 32)
        
        Returns:
            Reference ID for bitmap array
        """
        img = Image.open(icon_path).resize((size, size)).convert("L")
        ref = self.xbmp.add_icon_from_pil(img)
        self.icon_refs[icon_name] = ref
        return ref
    
    def add_material_icon(self, symbol: str, icon_name: str, size: int = 24) -> Optional[str]:
        """
        Add Material Icon from existing icon files
        
        Args:
            symbol: Material icon symbol (e.g., "mi_home_24px")
            icon_name: Name for C reference
            size: Target size
        
        Returns:
            Reference ID or None if icon file not found
        """
        # Look for icon in common paths
        search_paths = [
            Path("assets/icons"),
            Path("ui/icons"),
            Path(f"assets/material_icons/{size}px"),
        ]
        
        for base_path in search_paths:
            icon_file = base_path / f"{symbol}.png"
            if icon_file.exists():
                return self.add_icon_from_file(str(icon_file), icon_name, size)
        
        return None
    
    def export_to_header(self, output_path: str = "") -> str:
        """
        Export all icons as C header file
        
        Args:
            output_path: Output file path (empty string = return only, don't save)
        
        Returns:
            Generated C code
        """
        c_code = self.xbmp.generate_c_code()
        
        # Add icon references at the end
        header = [
            "#pragma once",
            "#include <stdint.h>",
            "",
            "/* Auto-generated icon bitmaps for ESPOS UI */",
            "/* Generated by UI Designer - Image Tools Integration */",
            "",
        ]
        
        footer = [
            "",
            "/* Icon reference lookup table */",
            "typedef struct {",
            "    const char* name;",
            "    const unsigned char* bitmap;",
            "    uint8_t width;",
            "    uint8_t height;",
            "} IconRef;",
            "",
        ]
        
        # Add individual icon references
        refs_list = []
        for name, ref in self.icon_refs.items():
            # Extract size from bitmap comment (e.g., "/* 16x16, 32 bytes */")
            # For now, assume 16x16 or 24x24
            size = 16  # TODO: parse from XBMP manager
            footer.append(f"#define ICON_{name.upper()} {ref}")
            refs_list.append(f'    {{"{name}", {ref}, {size}, {size}}}')
        
        footer.extend([
            "",
            "static const IconRef UI_ICONS[] = {",
            ",\n".join(refs_list),
            "};",
            "",
            f"#define UI_ICON_COUNT {len(refs_list)}",
        ])
        
        full_code = "\n".join(header) + c_code + "\n".join(footer)
        
        if output_path:
            Path(output_path).write_text(full_code, encoding="utf-8")
        
        return full_code
    
    def get_stats(self) -> Dict[str, int]:
        """Get deduplication statistics"""
        return {
            "total_icons": len(self.icon_refs),
            "unique_bitmaps": len(self.xbmp.bitmaps),
            "duplicates_saved": len(self.icon_refs) - len(self.xbmp.bitmaps),
            "memory_saved_percent": int((1 - len(self.xbmp.bitmaps) / max(1, len(self.icon_refs))) * 100)
        }


