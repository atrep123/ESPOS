def cli_export_icons(icon_dir: str, output_file: str, size: int = 16):
    """
    Command-line icon export
    
    Usage:
        python -c "from ui_designer_image_tools import cli_export_icons; 
                   cli_export_icons('assets/icons', 'output/icons.h', 16)"
    """
    if not PIL_AVAILABLE:
        print("[ERROR] PIL/Pillow not installed")
        return
    
    exporter = IconBitmapExporter()
    icon_path = Path(icon_dir)
    
    if not icon_path.exists():
        print(f"[ERROR] Icon directory not found: {icon_dir}")
        return
    
    count = 0
    for icon_file in icon_path.glob("*.png"):
        name = icon_file.stem
        try:
            exporter.add_icon_from_file(str(icon_file), name, size)
            count += 1
        except Exception as e:
            print(f"[WARN] Skipped {name}: {e}")
    
    if count == 0:
        print("[ERROR] No icons found")
        return
    
    exporter.export_to_header(output_file)
    stats = exporter.get_stats()
    
    print(f"[OK] Exported {count} icons to {output_file}")
    print(f"     Unique bitmaps: {stats['unique_bitmaps']}")
    print(f"     Memory saved: {stats['memory_saved_percent']}%")


def cli_export_font(bdf_file: str, output_file: str, text_sample: Optional[str] = None):
    """
    Command-line font export
    
    Usage:
        python -c "from ui_designer_image_tools import cli_export_font;
                   cli_export_font('fonts/terminus-12.bdf', 'output/font.h', 'Hello')"
    """
    font_exporter = FontExporter()
    
    if not Path(bdf_file).exists():
        print(f"[ERROR] Font file not found: {bdf_file}")
        return
    
    font_name = Path(bdf_file).stem
    font = font_exporter.load_font(bdf_file, font_name)
    
    if text_sample:
        # Subset export
        c_code = font_exporter.export_font_subset(font_name, text_sample, output_file)
        print(f"[OK] Exported font subset ({len(set(text_sample))} unique chars) to {output_file}")
    else:
        # Full ASCII export
        c_code = font_exporter.export_full_font(font_name, output_file)
        print(f"[OK] Exported full font to {output_file}")


