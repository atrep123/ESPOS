def export_current_scene_assets(designer, output_dir: str) -> Dict[str, str]:
    """
    Export all assets (icons, fonts) used in current scene
    
    Args:
        designer: UIDesigner instance
        output_dir: Output directory for generated files
    
    Returns:
        Dict of generated files {filename: content}
    """
    output_path = Path(output_dir)
    output_path.mkdir(parents=True, exist_ok=True)
    
    generated_files = {}
    
    # TODO: Scan scene for used icons and text
    # For now, just demonstrate the API
    
    # Example: Export common UI icons
    if PIL_AVAILABLE:
        try:
            exporter = IconBitmapExporter()
            
            # Check if user has icon assets
            icon_dir = Path("assets/icons")
            if icon_dir.exists():
                for icon_file in icon_dir.glob("*.png"):
                    name = icon_file.stem
                    try:
                        exporter.add_icon_from_file(str(icon_file), name)
                    except Exception as e:
                        print(f"[WARN] Failed to add icon {name}: {e}")
            
            # Generate header
            icon_header = output_path / "ui_icons.h"
            c_code = exporter.export_to_header(str(icon_header))
            generated_files["ui_icons.h"] = c_code
            
            # Print stats
            stats = exporter.get_stats()
            print(f"[OK] Icon export: {stats['total_icons']} icons, "
                  f"{stats['unique_bitmaps']} unique, "
                  f"{stats['memory_saved_percent']}% saved")
        
        except Exception as e:
            print(f"[WARN] Icon export failed: {e}")
    
    return generated_files


def find_bdf_fonts() -> List[str]:
    """Find available BDF fonts in common locations"""
    search_paths = [
        Path("fonts"),
        Path("assets/fonts"),
        Path("/usr/share/fonts/X11/misc"),  # Linux X11
        Path.home() / ".fonts",
    ]
    
    bdf_files = []
    for base_path in search_paths:
        if base_path.exists():
            bdf_files.extend([str(f) for f in base_path.glob("*.bdf")])
    
    return sorted(bdf_files)


